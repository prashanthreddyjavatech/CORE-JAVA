package arrays;

public class ArrayWithForEach {
	public static void main(String[] args) {
		
		int arr[]={10,20,30,40,50};  
		
		//printing array using for-each loop  
		
		for(int i:arr)  {
			System.out.println(i);  
			}
	
	int j[]= {10,20,30,40,50};
	for(int k:j) {
		System.out.println(k);
	}

}
}